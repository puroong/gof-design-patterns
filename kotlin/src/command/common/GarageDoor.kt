package command.common

class GarageDoor {
    fun up() {
        println("GarageDoor is Up")
    }

    fun down() {
        println("GarageDoor is Down")
    }
    fun stop() {
        println("GarageDoor is Stop")
    }
    fun lightOn() {
        println("GarageDoor is LightOn")
    }
    fun lightOff() {
        println("GarageDoor is LightOff")
    }
}